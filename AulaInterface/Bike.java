package AulaInterface;

public class Bike {
    
    private String cor;
    private String moddelo;
    private int ano;

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getModdelo() {
        return moddelo;
    }

    public void setModdelo(String moddelo) {
        this.moddelo = moddelo;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }
       
}
